"""Utilities to help with evaluation."""

from itertools import groupby
from uuid import UUID

from rich.console import Console
from rich.table import Table

from deet.data_models.base import Attribute
from deet.data_models.documents import (
    GoldStandardAnnotatedDocumentList,
    GoldStandardAnnotatedDocumentTypeVar,
)
from deet.data_models.evaluation import AttributeMetric, MetricType


def evaluate_llm_annotations(
    reference_documents: list[GoldStandardAnnotatedDocumentTypeVar],
    attributes: list[Attribute],
    llm_annotation_list: GoldStandardAnnotatedDocumentList,
    pipeline_run_id: UUID,
) -> list[AttributeMetric]:
    """
    Compare a list of human annotations to those generated by llms.
    Return a list of AttributeMetric objects.
    """
    metrics: list[AttributeMetric] = []
    for attribute in attributes:
        y_true = []
        y_pred = []
        for document in reference_documents:
            y_true.append(document.get_attribute_value(attribute))

            llm_doc = llm_annotation_list.get_by_id(document.document.safe_identity)
            y_pred.append(llm_doc.get_attribute_value(attribute))

        metrics.extend(
            AttributeMetric(
                attribute=attribute,
                pipeline_run_id=pipeline_run_id,
                metric=metric,
                value=metric.calculate(y_true, y_pred),
            )
            for metric in MetricType
        )

    return metrics


def display_metrics(metrics: list[AttributeMetric]) -> None:
    """Print metrics in a nice table to the command line."""
    console = Console()

    table = Table(title="Evaluation Results")

    metric_names = sorted({str(m.metric) for m in metrics})

    # Create table with metrics as columns
    table = Table(title="Evaluation Metrics")
    table.add_column("Attribute")
    for name in metric_names:
        table.add_column(name, justify="right")

    # Group metrics by attribute
    metrics_sorted = sorted(metrics, key=lambda m: m.attribute.attribute_label)
    for attribute, group in groupby(
        metrics_sorted, key=lambda m: m.attribute.attribute_label
    ):
        row = [attribute]
        group_metrics = {str(m.metric): m.value for m in group}
        # fill cells in order of metric_names
        row += [f"{group_metrics.get(name, 0.0):.4f}" for name in metric_names]
        table.add_row(*row)

    console.print(table)
